class Student:

    def __init__(self, name, major, gpa, in_probation):
        self.name = name
        self.major = major
        self.gpa = gpa
        self.in_probation = in_probation

# we can create our own data_type using classes and objects
# class is the structure of our data_type
# objects are the instances of that class
# inside the class, first we need to create a initialzing function(__init__)
# the parameters of the initializing functions are called attributes
# when we create an object we are actually calling the __init__ function, which acts as the struture of our data_type
